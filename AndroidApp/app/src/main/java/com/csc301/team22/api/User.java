package com.csc301.team22.api;

public class User {
    private int user_id;
    private String first_name;
    private String last_name;
    private String bio;

    public int getUser_id() {
        return user_id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public String getBio() {
        return bio;
    }
}
